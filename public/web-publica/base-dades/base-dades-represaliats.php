    <p><span class="text1 mitja raleway" style="margin-top:20px">
            El llistat de les persones represaliades pel franquisme està format per totes les persones terrassenques (residents o nascudes a Terrassa) que van patir la violència (afusellaments), la repressió (empresonament, detenció i tortures) i les depuracions (funcionariat o treballadors) per part del règim franquista i de forma directa, per motius polítics, ideològics, socials o culturals.</span> </p>

    <span class="more-text d-none text1 mitja raleway">
        <h3>Persones represaliades pel franquisme</h3>
        <p><strong>Contra l'oblit i per la dignitat i l'honor de les víctimes i de les persones represaliades pel franquisme a Terrassa.</strong> Veritat, Justícia i Reparació.</p>
        <p>Recuperar la memòria de les persones represaliades pel franquisme és un projecte de coneixement del passat, que es projecta en el present i que ens ha de servir per construir el futur.</p>
        <p>La memòria col·lectiva d’una ciutat o nació s’ha de construir sota els principis deontològics de la història, dels valors democràtics i dels drets humans, per considerar-se digna de ser anomenada memòria històrica i memòria democràtica. L’objectiu dels historiadors i historiadores, com a professionals, és la recerca de l’objectivitat històrica, però alhora, com a ciutadans, situar-nos al costat dels que han patit i pateixen la injustícia, és a dir, al costat de les víctimes i no pas del victimaris o botxins.</p>
        <p>El col·lectius que recull el llistat de les persones represaliades pel franquisme de l’EVMHT, en aquest moment són:</p>
        <ul>
            <li>Els terrassencs jutjats en consells de guerra sumaríssims i ordinaris (1936-1963) –Bando de guerra. Llei de 28 de juliol de 1936 de la Junta Nacional de Defensa–.</li>
            <li>Els jutjats pel Tribunal d’Ordre Públic (1963-1977) –Llei 154/1963, de 2 de desembre de 1963–.</li>
            <li>Els depurats encausats i sancionats –Ley fijando normas para la depuración de funcionarios públicos. Llei de 10 febrer del 1939–.</li>
            <li>Les persones sotmeses a la llei de responsabilitats polítiques –Ley de Responsabilidades Políticas de 9 de febrer de 1939–.</li>
            <li>Les persones detingudes arbitràriament per les seves activitats antifranquistes –sigui a dependències policials o empresonades i no jutjades, incloent el dipòsit carcerari de Terrassa–.</li>
        </ul>
        <p>A Espanya, la nova <strong>Llei 20/2022, de 19 d’octubre, de Memòria Democràtica</strong>, segons l'Article 3, es considera víctima tota persona, independentment de la seva nacionalitat, que hagi patit dany físic, moral o psicològic, danys patrimonials, o menyscabament substancial dels seus drets fonamentals com a conseqüència de violacions de les normes internacionals de drets humans i del dret internacional humanitari durant el període del cop d'Estat del 18 de juliol de 1936, la Guerra d'Espanya i la Dictadura franquista, fins a l'entrada en vigor de la Constitució Espanyola de 1978. Això inclou persones mortes o desaparegudes, detingudes arbitràriament, torturades, exiliades, sotmeses a treballs forçats, represaliades econòmicament, entre d'altres. També es consideren víctimes els familiars d'aquestes persones i col·lectius específics com les persones LGTBI, depurades professionalment, i nens sostrets, entre d'altres.</p>
        <p>La llei de Memòria Democràtica declara:</p>
        <ul>
            <li>El caràcter il·legal i radicalment nul de totes les condemnes i sancions produïdes per raons polítiques, ideològiques, de consciència o creença religiosa durant la Guerra i la Dictadura Franquista fins el 1978 (Article 4).</li>
            <li>El dret de les víctimes a obtenir una declaració de reparació i reconeixement personal (Article 6).</li>
            <li>La celebració de dies d’homenatge –31 d'octubre, dia de record i homenatge a totes les víctimes del cop militar, la Guerra i la Dictadura, i el 8 de maig, dia d'homenatge a les víctimes de l'exili (Articles 7 i 8)–.</li>
            <li>Creació d’un registre i cens estatal de víctimes per garantir l'efectivitat dels principis de veritat, justícia, reparació i no repetició (Article 9).</li>
            <li>L'Administració General de l'Estat adoptarà mesures per al coneixement i commemoració dels fets representatius de la memòria democràtica i el reconeixement de les persones que van lluitar per la llibertat i la democràcia (Article 10), especialment fent un reconeixement explícit el paper actiu de les dones i reparar les formes especials de repressió o violència patida per elles (Article 11).</li>
        </ul>
        <p>Totes aquestes mesures busquen reconèixer, reparar i dignificar les víctimes de la Guerra i la Dictadura franquista, així com fomentar la memòria democràtica a Espanya.</p>
        <p>La nova llei de Memòria Democràtica de Catalunya, que serà aprovada al 2025, permetrà unificar totes les lleis vigents a Catalunya i Espanya –Llei de Memorial Democràtic de 2007, Ley de Fosas de 2009, Ley de Reparación Jurídica de las víctimas del franquismo del 2017 i Ley de Memoria Democrática de 2022–.</p>
        <p>Tota la legislació deixa molt clara la situació legal de les víctimes del franquisme, ja que ha declarat il·legal el règim franquista i els seus tribunals, per la qual cosa s’anul·len totes les seves sentències i condemnes. Era una demanda històrica del moviment memorialista, car com hem explicat, aquests tribunals militars, instaurats un cop acabada la guerra, estaven mancats de qualsevol legitimitat i tenien un caràcter inquisitorial i repressiu, on la possibilitat de defensa era nul·la. Dins dels col·lectius estudiats a l’EVMHT, els afusellats i els deportats –com explicarem en el llistat de l’exili– són els que més van pagar la seva lluita per la llibertat i la democràcia social contra la dictadura i el feixisme, perdent la seva vida i són els que encara no han estat reconeguts com a represaliats a la nostra ciutat i arreu de l’estat espanyol.</p>
        <p>Per la qual cosa ens correspon a tots nosaltres: historiadors, societat civil organitzada en les col·lectius memorialistes i les diferents administracions, restaurar el bon nom d’aquests ciutadans terrassencs i realitzar un reconeixement ciutadà de forma ineludible. Dur a terme un homenatge i un monument dedicat a les persones afusellades per la Dictadura Franquista esdevé un gest imprescindible de la ciutat per recuperar la seva dignitat anihilada pel franquisme a aquestes persones.</p>
        <p>L’equidistància davant dels crims contra la humanitat que va dur a terme el règim franquista és intolerable i una evidència del suport que molts encara donen a un règim brutal, inhumà i criminal que ens va dur a la destrucció, la violència, la guerra i la mort. A Europa i al món, cap persona civilitzada gosaria defensar els règims nazis o feixistes, homònims del franquisme, com succeeix al nostre país, encara avui, on observem, tristament, un ressorgir dels nostàlgics del franquisme i el feixisme.</p>
        <p><em>"Homenatjar als afusellats, no pot entendre’s de cap manera com a un menyspreu vers les víctimes de la repressió produïda durant els primers mesos de la guerra civil."</em></p>
        <p>Les institucions democràtiques terrassenques, catalanes i espanyoles estan fent un gran esforç perquè les noves generacions coneguin aquest terrible passat que vàrem viure a casa nostra, i alhora evitar la seva reaparició. Els i les historiadores som part fonamental en aquest procés, per això, recuperar la memòria dels afusellats terrassencs és un projecte de coneixement del passat, que es projecta en el present i ens ha de servir per construir el futur.</p>
        <p>El grup d’historiadors/es que estem treballant, des de fa més de vint anys, la recuperació de la memòria històrica, que hem de donar suport el projecte municipal de les Stolperstein, pensem que ara és imprescindible donar suport a l’estudi i homenatge als afusellats per la dictadura i donar a conèixer la tràgica història que els va portar a la mort. A l’EVMHT donarem a conèixer totes les biografies i altres dades sobre els afusellats –al voltant de cinquanta– als mitjans de comunicació locals i les xarxes socials, amb un doble objectiu: divulgar les nostres investigacions i intentar retrobar als familiars d’aquestes persones, per poder retre’ls un homenatge públic, el 15 d’octubre de 2025, “Dia Nacional en Memòria de les Víctimes de la Guerra Civil i les Víctimes de la Repressió de la Dictadura Franquista”.</p>
        <p>Homenatjar als afusellats, no pot entendre’s de cap manera com a un menyspreu vers les víctimes de la repressió produïda durant els primers mesos de la guerra civil a la nostra ciutat, per part de les anomenades patrulles de control. Al contrari, l’objectiu és entendre els fets i poder demostrar, que aquestes persones van ser afusellades essent innocents i, en cas d’haver estat culpables, van ser condemnades, sense la possibilitat de tenir un judici just. Les proves de les acusacions eren les úniques veus que s’escoltaven als judicis farsa –i sempre en forma de declaracions textuals– i mai les de les defenses, que estaven absents, només cartes de suport, que podien servir d’atenuant i que els tribunals formats per militars (inclòs el defensor) no tenien mai presents al llarg del consell de guerra, fos sumaríssim o ordinari.</p>
        <p>A Terrassa, els estudis realitzats pel Centre d’Estudis Històrics de Terrassa sobre les víctimes de la guerra civil sempre hem tingut presents a totes les víctimes sense cap mena d’exclusió. Construir una memòria col·lectiva democràtica no ens pot fer oblidar que la guerra i la violència exercida contra els nostres veïns i veïnes va tenir uns orígens i uns responsables, la qual cosa ens obliga a graduar aquesta responsabilitat, no amb l’objectiu d’exculpar als responsables, sinó més aviat de demostrar que sense aquestes accions principals i detonants no s’haguessin comès els crims terribles que després vàrem haver de patir.</p>
        <p>El cop d’estat franquista va destruir el règim democràtic republicà desencadenant una terrible guerra civil, on es varen desfermar totes les violències pròpies d’un conflicte bèl·lic i, alhora, d’un conflicte molt més gran, la lluita entre la democràcia i el nazi-feixisme, durant la Segona Guerra Mundial. Els homes i dones, que varen viure aquella terrible situació a Espanya, van ser les primeres víctimes del primer acte d’una de les guerres més violentes, inhumanes i terribles que ha patit la humanitat, més de cinquanta milions de morts.</p>
        <p>Per això, recordar el passat és una obligació moral de tots i totes.</p>
        <p><strong>Memòria és democràcia.</strong></p>

