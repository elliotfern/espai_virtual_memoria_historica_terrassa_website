// Función para obtener la URL actual
export const getCurrentUrl = (): string => {
  return window.location.pathname;
};

// Función para generar el menú
export const generateMenu = (links: Record<string, MenuLink>) => {
  const currentUrl = getCurrentUrl();
  const nav = document.createElement("ul");
  nav.classList.add("nav", "nav-pills", "flex-column", "mb-auto");

  for (const data of Object.values(links)) {
    const listItem = document.createElement("li");
    listItem.classList.add("nav-item");

    const anchor = document.createElement("a");
    anchor.href = data.url;
    // Asegúrate de que la clase activa no esté vacía
    const activeClass = currentUrl.startsWith(data.url) ? "active" : "";
    if (activeClass) {
      anchor.classList.add("nav-link", activeClass);
    } else {
      anchor.classList.add("nav-link");
    }

    const svgIcon = document.createElementNS(
      "http://www.w3.org/2000/svg",
      "svg"
    );
    svgIcon.setAttribute("xmlns", "http://www.w3.org/2000/svg");
    svgIcon.setAttribute("width", "16");
    svgIcon.setAttribute("height", "16");
    svgIcon.setAttribute("fill", "currentColor");
    const iconClasses = data.icon.split(" "); // Separar las clases en un array
    svgIcon.classList.add("text-white", "bi", ...iconClasses, "me-2");
    svgIcon.setAttribute("viewBox", "0 0 16 16");

    // Añadir paths al icono
    data.paths.forEach((pathData) => {
      const path = document.createElementNS(
        "http://www.w3.org/2000/svg",
        "path"
      );
      path.setAttribute("d", pathData);
      svgIcon.appendChild(path);
    });

    anchor.appendChild(svgIcon);

    // Añadir el label
    const spanLabel = document.createElement("span");
    spanLabel.classList.add("text-white", "links-sidebar");
    spanLabel.innerHTML = data.label;

    anchor.appendChild(spanLabel);
    listItem.appendChild(anchor);
    nav.appendChild(listItem);
  }

  // Añadir el menú al DOM
  document.getElementById("menuContainer")?.appendChild(nav);
};
